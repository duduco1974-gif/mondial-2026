// ============================================================
// GENERATE MATCHES — sorted by date
// ============================================================
function generateGroupMatches(groupKey) {
  const group = GROUPS[groupKey];
  const matches = [];
  for (let i = 0; i < 4; i++) {
    for (let j = i + 1; j < 4; j++) {
      const id = `${groupKey}-${i}-${j}`;
      const sched = MATCH_SCHEDULE[id] || {};
      matches.push({
        id,
        group: groupKey,
        home: group.teams[i],
        away: group.teams[j],
        date: sched.date || null,
        venue: sched.venue || null,
        score: null,
        status: 'upcoming'
      });
    }
  }
  return matches.sort((a, b) => {
    if (!a.date) return 1;
    if (!b.date) return -1;
    return new Date(a.date) - new Date(b.date);
  });
}

const ALL_MATCHES = Object.keys(GROUPS).reduce((acc, key) => {
  return acc.concat(generateGroupMatches(key));
}, []).sort((a, b) => {
  if (!a.date) return 1;
  if (!b.date) return -1;
  return new Date(a.date) - new Date(b.date);
});
